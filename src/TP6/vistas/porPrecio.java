    private void jtPrecioMenorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtPrecioMenorKeyReleased
        borrarFilas();
        double producto,producto2;
        try{
        producto=Double.parseDouble(jtPrecioMenor.getText());
        if(jtPrecioMenor.getText().equals("")){
            producto2=mayorPrecio();
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ingrese el precio en decimales!");
            return;
        }
        
        try{
        producto2=Double.parseDouble(jtPrecioMayor.getText());
        if(jtPrecioMayor.getText().equals("")){
            producto2=mayorPrecio();
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ingrese el precio en decimales!");
            return;
        }
        
        for(Producto p:productos){
            if(p.getPrecio()>=producto && p.getPrecio()<=producto2){
            modelo.addRow(new Object[]{p.getCodigo(),p.getDescripcion(),p.getPrecio(),p.getStock()});
            }
        }
    }//GEN-LAST:event_jtPrecioMenorKeyReleased

