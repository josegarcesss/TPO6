    private void borrarFilas(){
        int filas=jtPrecio.getRowCount()-1;        
        for (int f = filas; f >= 0; f--) {
            modelo.removeRow(f);
        }
    }
    
    private double mayorPrecio(){
        double mayor= 0;
        for (Producto producto : productos) {
           if(producto.getPrecio()>=mayor){
               mayor=producto.getPrecio();
           } 
        }
        return mayor;
    }
    
    
    

